import regex

from maskflow.detectors.regex_base import RegexDetector

# Поддерживаемые форматы:
#   +7 (999) 123-45-67   ← пробел + скобки
#   +7-999-123-45-67     ← тире
#   8 999 123 45 67      ← пробелы
#   89991234567          ← без разделителей
#   8(999)1234567        ← скобки без пробелов
#
# Стратегия (без катастрофического backtracking):
#   Код области распознаётся двумя явными альтернативами:
#     1) опциональный разделитель + «(DDD)» + опциональный разделитель
#     2) опциональный разделитель + DDD + опциональный разделитель
#   В каждой позиции допускается не более 1 разделителя —
#   это исключает ложные срабатывания на «8---123---45--67».
PHONE_REGEX = regex.compile(
    r"""
    (?P<phone>
        (?<!\d)
        (?:\+7|8)

        # Код области: со скобками или без
        (?:
            [\s\-]? \( \d{3} \) [\s\-]?    # +7 (999) или 8(999)
            |
            [\s\-]? \d{3} [\s\-]?           # +7-999- или 8 999 (один разделитель с каждой стороны)
        )

        \d{3}
        [\s\-]?
        \d{2}
        [\s\-]?
        \d{2}
        (?!\d)
    )
    """,
    regex.VERBOSE,
)


class PhoneDetector(RegexDetector):
    name = "phone"

    def __init__(self) -> None:
        super().__init__(PHONE_REGEX)
